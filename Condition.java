public class Condition {

    public static void main(String [] args)
    {
        ifelsecond(2);
    }
    public static int ifelsecond(int a)
    {
        if (a==2)
        {
            System.out.println("Condition is correct");
        }
        else
        {
            System.out.println("Condition is incorrect");
        }
        return a;
    }

}
